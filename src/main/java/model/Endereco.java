package model;

import java.io.Serializable;
import java.util.Objects;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

/**
 *
 * @author salesfilho
 */
@Entity
@SequenceGenerator(name = "ID_ENDERECO_SEQUENCE", sequenceName = "SQ_ENDERECO", allocationSize = 1)

public class Endereco implements Serializable {

    @Id
    @GeneratedValue(generator = "ID_ENDERECO_SEQUENCE", strategy = GenerationType.SEQUENCE)
    private Long id;

    private String logradouro;
    private String bairro;
    private String cep;
    private Integer numero;
    private String cidade;
    private String estado;
    private String pais;
    private boolean comercial;
    
        
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        this.logradouro = logradouro;
    }

    public String getBairro() {
        return bairro;
    }

    public void setBairro(String bairro) {
        this.bairro = bairro;
    }

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public Integer getNumero() {
        return numero;
    }

    public void setNumero(Integer numero) {
        this.numero = numero;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public boolean isComercial() {
        return comercial;
    }

    public void setComercial(boolean comercial) {
        this.comercial = comercial;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 11 * hash + Objects.hashCode(this.id);
        hash = 11 * hash + Objects.hashCode(this.logradouro);
        hash = 11 * hash + Objects.hashCode(this.bairro);
        hash = 11 * hash + Objects.hashCode(this.cep);
        hash = 11 * hash + Objects.hashCode(this.numero);
        hash = 11 * hash + Objects.hashCode(this.cidade);
        hash = 11 * hash + Objects.hashCode(this.estado);
        hash = 11 * hash + Objects.hashCode(this.pais);
        hash = 11 * hash + (this.comercial ? 1 : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Endereco other = (Endereco) obj;
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        if (!Objects.equals(this.logradouro, other.logradouro)) {
            return false;
        }
        if (!Objects.equals(this.bairro, other.bairro)) {
            return false;
        }
        if (!Objects.equals(this.cep, other.cep)) {
            return false;
        }
        if (!Objects.equals(this.numero, other.numero)) {
            return false;
        }
        if (!Objects.equals(this.cidade, other.cidade)) {
            return false;
        }
        if (!Objects.equals(this.estado, other.estado)) {
            return false;
        }
        if (!Objects.equals(this.pais, other.pais)) {
            return false;
        }
        if (this.comercial != other.comercial) {
            return false;
        }
        return true;
    }

}
